# Function to query the ChromaDB knowledge base
def query_knowledge_base(question, k=3):
    question_lower = question.lower()

    # --- Exact Match for TeamID ---
    if 'TeamID' in df.columns:
        matching_team = df[
            df['TeamID'].astype(str).str.lower()
            == question_lower
        ]
        if not matching_team.empty:
            team_name = matching_team.iloc[0]['TeamName']
            response = f"""
--- Team Details ---

Team ID: {matching_team.iloc[0]['TeamID']}
Team Name: {team_name}
"""
            members = matching_team[
                ['MemberName','Role']
            ].drop_duplicates()
            response += "\nMembers:\n"
            for _, row in members.iterrows():
                response += (
                    f"- {row['MemberName']} "
                    f"({row['Role']})\n"
                )
            return response

    # --- Exact Match for MemberID ---
    if 'MemberID' in df.columns:
        matching_member_id = df[
            df['MemberID'].astype(str).str.lower()
            == question_lower
        ]
        if not matching_member_id.empty:
            row = matching_member_id.iloc[0]
            return f"""
--- Member Details ---

Member ID: {row['MemberID']}
Member Name: {row['MemberName']}
Team: {row['TeamName']}
Role: {row['Role']}
Project: {row['ProjectName']}
Status: {row['ProjectStatus']}
Responsibilities: {row['Responsibilities']}
"""

    # --- Status Search ---
    if question_lower in [
        "completed",
        "ongoing"
    ]:
        projects = df[
            df['ProjectStatus']
            .str.lower() == question_lower
        ]
        response = (
            f"\nProjects with status "
            f"'{question}':\n\n"
        )
        for project in projects[
            'ProjectName'
        ].unique():
            response += f"- {project}\n"
        return response

    # --- Role Search ---
    roles = [
        "frontend developer",
        "backend developer",
        "data analyst",
        "project manager"
    ]
    if question_lower in roles:
        results = df[
            df['Role']
            .str.lower() == question_lower
        ]
        response = (
            f"\nMembers with role "
            f"{question}:\n\n"
        )
        for _, row in results.iterrows():
            response += (
                f"- {row['MemberName']} "
                f"({row['TeamName']})\n"
            )
        return response

    # --- Domain Search ---
    domains = [
        "business",
        "healthcare",
        "education",
        "retail",
        "agriculture",
        "cybersecurity",
        "transportation",
        "smart city",
        "hr tech",
        "events"
    ]
    if question_lower in domains:
        projects = df[
            df['ProjectDomain']
            .str.lower() == question_lower
        ]
        response = (
            f"\nProjects in "
            f"{question} domain:\n\n"
        )
        for _, row in projects.iterrows():
            response += (
                f"- {row['ProjectName']}"
                f" ({row['TeamName']})\n"
            )
        return response

    # --- 1. Exact Match for TeamName ---
    matching_teams = df[df['TeamName'].str.lower() == question_lower]
    if not matching_teams.empty:
        team_name = matching_teams['TeamName'].iloc[0]
        response = f"--- Details for Team: {team_name} ---\n"

        # Get unique projects and their statuses
        projects_info = matching_teams[['ProjectName', 'ProjectStatus']].drop_duplicates()
        response += "\nProjects:\n"
        for idx, row in projects_info.iterrows():
            response += f"- {row['ProjectName']} (Status: {row['ProjectStatus']})\n"

        # Get unique members and their roles
        members_info = matching_teams[['MemberName', 'Role']].drop_duplicates()
        response += "\nMembers:\n"
        for idx, row in members_info.iterrows():
            response += f"- {row['MemberName']} ({row['Role']})\n"

        return response

    # --- 2. Exact Match for MemberName ---
    matching_members = df[df['MemberName'].str.lower() == question_lower]
    if not matching_members.empty:
        member_name = matching_members['MemberName'].iloc[0]
        response = f"--- Details for Member: {member_name} ---\n"

        for idx, row in matching_members.drop_duplicates(subset=['MemberName', 'TeamName', 'ProjectName', 'Role', 'Responsibilities']).iterrows():
            response += f"\nTeam: {row['TeamName']}\n"
            response += f"Role: {row['Role']}\n"
            response += f"Project: {row['ProjectName']}\n"
            response += f"Responsibilities: {row['Responsibilities']}\n"
        return response

    # --- 3. Exact Match for ProjectName ---
    matching_projects = df[df['ProjectName'].str.lower() == question_lower]
    if not matching_projects.empty:
        project_name = matching_projects['ProjectName'].iloc[0]
        response = f"--- Details for Project: {project_name} ---\n"

        project_data = matching_projects.iloc[0] # Take first row for common project details
        response += f"\nTeam: {project_data['TeamName']}\n"
        response += f"Status: {project_data['ProjectStatus']}\n"
        response += f"Tech Stack: {project_data['TechStack']}\n"
        response += f"Problem Statement: {project_data['ProblemStatement']}\n"
        response += f"Solution: {project_data['Solution']}\n"

        # Get unique members for this project
        members_info = matching_projects[['MemberName', 'Role']].drop_duplicates()
        response += "\nMembers on Project:\n"
        for idx, row in members_info.iterrows():
            response += f"- {row['MemberName']} ({row['Role']})\n"
        return response

    # --- 4. Fallback to ChromaDB Semantic Search with RAG ---
    # Convert the question to an embedding
    question_embedding = model.encode([question]).tolist()[0]

    # Query ChromaDB for similar documents
    results = collection.query(
        query_embeddings=[question_embedding],
        n_results=k,
        include=['documents', 'metadatas']
    )

    # Build context from the retrieved documents
    if not results['documents'][0]:
        return "No relevant information found via semantic search."

    context = "\n\n".join(
        [doc for doc in results['documents'][0] if doc] # Filter out any potential empty documents
    )

    # Gemini Prompt
    prompt = f"""
You are a Team Knowledge Assistant.

Answer ONLY from the provided context.

Context:
{context}

Question:
{question}

Provide a clear and concise answer.
"""
    # Use the globally available 'gemini_client' for Gemini
    response = gemini_client.models.generate_content(
        model="gemini-2.5-flash",
        contents=prompt
    )

    return response.text

