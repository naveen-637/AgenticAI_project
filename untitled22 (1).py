# Convert each row into a document string
def row_to_document(row):
    document_parts = []
    for col, value in row.items():
        # Skip TeamID as it's primarily an identifier, not descriptive content
        if col == 'TeamID':
            continue
        if pd.notna(value):
            document_parts.append(f"{col.replace(' ', '')}: {value}")
    return "\n".join(document_parts)

